	function global:sortIngredients($recipes)
	{
		$array = @()
		$global:list = @{ }
		foreach ($recipe in $recipes) { 
			$recpPath = "$($StoragePath)Ingredients\$($recipe).txt"
			$r = Get-Content -Path $recpPath | convertfrom-json
			$array += ($r)
		}
		
		Write-Host "Array: $($array)"
		
		foreach ($array_item in $array)
		{
			$c = 0 #failsafe if something messes up. 
			switch -Wildcard ($array_item.Unit)
			{
				"*Gallons*"{
					$amo = 128 * ($array_item.Qty)
					$mes = 'Fluid'
				}
				
				"*Quarts*"{
					$amo = 32 * ($array_item.Qty)
					$mes = 'Fluid'
				}
				
				"*Pints*"{
					$amo = 16 * ($array_item.Qty)
					$mes = 'Fluid'
				}
				
				"*Liters*"{
					$amo = 33.814 * ($array_item.Qty)
					$mes = 'Fluid'
				}
				
				"*Fluid Cups*"{
					$amo = 8 * ($array_item.Qty)
					$mes = 'Fluid'
				}
				
				"*Fluid Oz*"{
					$amo = 1 * ($array_item.Qty)
					$mes = 'Fluid'
				}
				
				"*Fluid Tbsp*"{
					$amo = 0.5 * ($array_item.Qty)
					$mes = 'Fluid'
				}
				
				"*Fluid Tsp*"{
					$amo = 0.166 * ($array_item.Qty)
					$mes = 'Fluid'
				}
				
				"*Fluid mL*"{
					$amo = 0.0339 * ($array_item.Qty)
					$mes = 'Fluid'
				}
				
				"*Dry Cups*"{
					$amo = 226.8 * ($array_item.Qty)
					$mes = 'Dry'
				}
				
				"*Dry Oz*"{
					$amo = 28.35 * ($array_item.Qty)
					$mes = 'Dry'
				}
				
				"*Dry Tbsp*"{
					$amo = 14.155 * ($array_item.Qty)
					$mes = 'Dry'
				}
				
				"*Dry Tsp*"{
					$amo = 4.766 * ($array_item.Qty)
					$mes = 'Dry'
				}
				
				"*Pounds*"{
					$amo = 453.6 * ($array_item.Qty)
					$mes = 'Dry'
				}
				
				"*Kilogram*"{
					$amo = 0.001 * ($array_item.Qty)
					$mes = 'Dry'
				}
				
				"*Grams*"{
					$amo = 1 * ($array_item.Qty)
					$mes = 'Dry'
				}
				
				default
				{
					$amo = 1 * ($array_item.Qty)
					$mes = 'Other'
				}
			}
			
			Write-Host "amo: $($amo)"
			Write-Host "mes: $($mes)"
			$n = "$($array_item.Name)-$mes"
			Write-Host "N: $($n)"
			if ($list[$n])
			{
				if ($list[$n].Measure -eq $mes)
				{
					$add = $amo
					$list[$n].Amount += ($add)
				}
				else
				{
					$n = "$($array_item.Name)-$mes-$c"
					$c++
					$list[$n] = @{ 'Name' = ($array_item.Name); 'Location' = ($array_item.Type); 'Unit' = ($array_item.Unit); 'Amount' = $amo; 'Measure' = $mes; 'ID' = ($array_item.ID); }
				}
			}
			else
			{
				$list[$n] = @{ 'Name' = ($array_item.Name); 'Location' = ($array_item.Type); 'Unit' = ($array_item.Unit); 'Amount' = $amo; 'Measure' = $mes; 'ID' = ($array_item.ID); }
			}
		}
		Write-Host  "-----------"
		Write-Host "Inline"
		Write-Host "$list"
		Write-Host "-----------"
		
	}
	
